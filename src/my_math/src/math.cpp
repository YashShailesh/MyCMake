#include "math.hpp"

float MathClass::add_function(float first_no, float second_no)
{
    return first_no + second_no;
}

float MathClass::div_function(float first_no, float second_no)
{
    return first_no / second_no;
}

float MathClass::mul_function(float first_no, float second_no)
{
    return first_no * second_no;
}

float MathClass::sub_function(float first_no, float second_no)
{
    return first_no - second_no;
}